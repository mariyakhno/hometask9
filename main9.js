// Приклад структири знаходиться у файлі example.png

for (const course of coursesArray) {
    let courseDiv = document.createElement('div');
    let titleDiv = document.createElement('div');
    let monthDurDiv = document.createElement('div');
    let hourDurDiv = document.createElement('div');
    let moduleDiv = document.createElement('ul');

    titleDiv.innerText = course.title;
    monthDurDiv.innerText = `Month duration : ${course.monthDuration}`;
    hourDurDiv.innerText = `Hour duration : ${course.hourDuration}`;

    for (const key in course) {
        if (Array.isArray(course[key])) {
            for (const item of course[key]) {
                let moduleName = document.createElement('li')
                moduleName.innerText = item
                moduleDiv.appendChild(moduleName)
            }
        }
    }

    document.body.appendChild(courseDiv);
    courseDiv.append(titleDiv, monthDurDiv, hourDurDiv, moduleDiv);

    courseDiv.classList.add('course-wrapper');
    titleDiv.classList.add('title');
    monthDurDiv.classList.add('month-duration');
    hourDurDiv.classList.add('hour-duration');
    moduleDiv.classList.add('module');
}

